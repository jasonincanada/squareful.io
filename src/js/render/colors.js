
export function colorForSize(size) {
    switch (size) {
        case 1 : return "#ffffff";
        case 2 : return "#b7b7b7";
        case 3 : return "#78cfa0";
        case 4 : return "#dddddd";
        case 5 : return "#ef9a91";
        case 6 : return "#c7e6d5";
        case 7 : return "#9fcfec";
        case 8 : return "#ffffff";
        default: return "#ffffff";
    }
}
